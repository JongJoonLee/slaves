package com.groupware.www.Vo;

public class TimelineVO {
	
	
	private int time_no =0;
	private int mem_no =0;
	private String time_contents= null;	
	private String time_date=null;
	public int getTime_no() {
		return time_no;
	}
	public void setTime_no(int time_no) {
		this.time_no = time_no;
	}
	public int getMem_no() {
		return mem_no;
	}
	public void setMem_no(int mem_no) {
		this.mem_no = mem_no;
	}
	public String getTime_contents() {
		return time_contents;
	}
	public void setTime_contents(String time_contents) {
		this.time_contents = time_contents;
	}
	public String getTime_date() {
		return time_date;
	}
	public void setTime_date(String time_date) {
		this.time_date = time_date;
	}
	
	
	

}
